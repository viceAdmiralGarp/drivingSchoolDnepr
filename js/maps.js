locationSelect.addEventListener("change", function () {
    const selectedLocation = locationSelect.value;
    if (selectedCity === "dnipro") {
        locationDetails.innerHTML = dniproPoints[selectedLocation](getScreenSizeMap(windowWidth).mapW, getScreenSizeMap(windowWidth).mapH);
    } else if (selectedCity === "kamanskoe") {
        locationDetails.innerHTML = kamenskoePoints[selectedLocation](getScreenSizeMap(windowWidth).mapW, getScreenSizeMap(windowWidth).mapH);
    }
});

function getScreenSizeMap(windowWidth) {
    if (windowWidth <= 576)
        return { element: '576', mapW: '400', mapH: '300' }
    else if (windowWidth <= 768)
        return { element: '768', mapW: '576', mapH: '450' }
    else if (windowWidth <= 992)
        return { element: '992', mapW: '768', mapH: '450' }
    else if (windowWidth <= 1199)
        return { element: '1199', mapW: '900', mapH: '600' }
    else if (windowWidth > 1199)
        return { element: '1400', mapW: '1200', mapH: '600' }
}